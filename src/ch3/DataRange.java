package ch3;

public class DataRange {
	public static void main(String[] args) {
		System.out.printf("Short \tRange:%d-%d%n", Short.MIN_VALUE, Short.MAX_VALUE);
		System.out.printf("Integer \tRange:%d-%d%n", Integer.MIN_VALUE, Integer.MAX_VALUE);
		System.out.printf("Long \tRange:%d-%d%n", Long.MIN_VALUE, Long.MAX_VALUE);
		System.out.printf("Byte \tRange:%d-%d%n", Byte.MIN_VALUE, Byte.MAX_VALUE);
		System.out.printf("Float \tRange:%e-%e%n", Float.MIN_VALUE, Float.MAX_VALUE);
		System.out.printf("Double \tRange:%e-%e%n", Double.MIN_VALUE, Double.MAX_VALUE);

	}
}
