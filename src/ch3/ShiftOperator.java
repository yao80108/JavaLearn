package ch3;

public class ShiftOperator {
	public static void main(String[] args) {
		int number = 1;
		System.out.println(number);

		number = number << 1;
		System.out.println(number);

		number = number << 1;
		System.out.println(number);

		number = number << 1;
		System.out.println(number);
	}
}
